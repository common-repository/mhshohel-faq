<h2>Mhshohel Faqs Documentation</h2>

<p>Download Mhshohel faqs plugin from wordpress directory from <a href="http://wordpress.org/plugins/mhshohel-faq" target="_blank">http://wordpress.org/plugins/mhshohel-faq</a></p>

<p>Unzip it and copy to your wordpress plugin directory</p>

<p>Active Plugin by clicking active link from wordpress plugin page</p>

<p>Add New Faq by clicking Add faq</p>

<img src="<?php echo get_option('siteurl') . '/wp-content/plugins/mhshohel-faq'; ?>/documentation-img/add-new-faq.jpg" alt=""  />

<p>You can save by categories.</p>

<h3>Call faqs with shortcode</h3>

<p>You can call faqs in wordpress post or page via shortcode.</p>

<p>For simply call all faqs, use [mhshohel_faq]</p>

<p>Call faq from categories, use [mhshohel_faq category="category id"] use category ID</p>

<p>Call faq by order, use [mhshohel_faq order="DESC"] DESC or ASC</p>

<p>Faq limit, use [mhshohel_faq limit="20"] use numaric.</p>

<p>All Together, use [mhshohel_faq limit="20" category="category id" order="DESC" ]</p>

